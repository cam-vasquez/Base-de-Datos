-- Tables section.
CREATE TABLE FABRICANTE(
	id INT PRIMARY KEY,
	nombre VARCHAR (50)
);

CREATE TABLE AVION(
	id INT PRIMARY KEY,
	avion VARCHAR(10),
	capacidad VARCHAR(20) NOT NULL,
	id_fabricante INT NOT NULL
);

CREATE TABLE PAIS(
	id INT PRIMARY KEY,
	nombre VARCHAR(50)
);

CREATE TABLE PASAJERO(
	id INT PRIMARY KEY,
	nombre VARCHAR(50)NOT NULL,
	pasaporte VARCHAR(15) NOT NULL,
	fecha_nacimiento DATE NULL,
	email VARCHAR(50) NOT NULL,
	clave VARCHAR(32) NOT NULL,
	id_pais INT NOT NULL
);

CREATE TABLE AEROPUERTO (
	id INT PRIMARY KEY,
	nombre VARCHAR(50) NOT NULL,
	id_pais INT NOT NULL
);

CREATE TABLE VUELO (
	id INT PRIMARY KEY,
	id_origen INT NOT NULL,
	id_destino INT NOT NULL,
	fecha_salida DATETIME,
	fecha_llegada DATETIME,
	id_avion INT NOT NULL
);

CREATE TABLE CLASE(
	id INT PRIMARY KEY,
	clase VARCHAR(15)
);

CREATE TABLE RESERVA (
	id INT PRIMARY KEY,
	costo MONEY NOT NULL,
	fecha DATE NOT NULL,
	id_pasajero INT NOT NULL,
	id_vuelo INT NOT NULL,
	id_clase INT NOT NULL
);

CREATE TABLE ASIENTO (
	id INT PRIMARY KEY,
	asiento CHAR(3) NOT NULL,
	id_reserva INT,
);

CREATE TABLE SERVICIO (
	id INT PRIMARY KEY,
	nombre VARCHAR(50) NOT NULL, 
	precio MONEY NOT NULL
);

CREATE TABLE EXTRA (
	id_servicio INT NOT NULL,
	id_reserva INT NOT NULL
);
GO
-- Primary/Foreign Key section.
-- PK Definition 
ALTER TABLE EXTRA ADD PRIMARY KEY (id_servicio, id_reserva);
-- FK Detinition
-- SERVICIO -> EXTRA
ALTER TABLE EXTRA ADD FOREIGN KEY (id_servicio) REFERENCES SERVICIO(id);
-- RESERVA -> EXTRA
ALTER TABLE EXTRA ADD FOREIGN KEY (id_reserva) REFERENCES RESERVA(id);
-- RESERVA -> ASIENTO
ALTER TABLE ASIENTO ADD FOREIGN KEY (id_reserva) REFERENCES RESERVA(id);
-- AVION -> VUELO
ALTER TABLE VUELO ADD FOREIGN KEY (id_avion) REFERENCES AVION(id);
-- FABRICANTE -> AVION
ALTER TABLE AVION ADD FOREIGN KEY (id_fabricante) REFERENCES FABRICANTE(id);
-- PAIS -> PASAJERO
ALTER TABLE PASAJERO ADD FOREIGN KEY (id_pais) REFERENCES PAIS(id);
-- PAIS -> AEROPUERTO
ALTER TABLE AEROPUERTO ADD FOREIGN KEY (id_pais) REFERENCES PAIS(id);
-- AEROPUERTO -> VUELO
ALTER TABLE VUELO ADD FOREIGN KEY (id_origen) REFERENCES AEROPUERTO (id);
ALTER TABLE VUELO ADD FOREIGN KEY (id_destino) REFERENCES AEROPUERTO (id);
-- PASAJERO -> RESERVA
ALTER TABLE RESERVA ADD FOREIGN KEY (id_pasajero) REFERENCES PASAJERO (id);
-- VUELO -> RESERVA
ALTER TABLE RESERVA ADD FOREIGN KEY (id_vuelo) REFERENCES VUELO(id);
-- CLASE -> RESERVA
ALTER TABLE RESERVA ADD FOREIGN KEY (id_clase) REFERENCES CLASE(id);
GO

